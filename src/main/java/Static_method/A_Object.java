package Static_method;

public class A_Object {
	
	public A_Object() {
		
		System.out.println("***********PARENT CLASS**********************");
		
	}
	
public A_Object(int i) {
		
		System.out.println("***********PARENT CLASS**********************");
		System.out.println("The value of i is......"+i);
	}

public A_Object(int i, int j) {
	
	System.out.println("***********PARENT CLASS**********************");
	System.out.println("The value of i is......"+i);
	System.out.println("The value of i is......"+j);
}

}